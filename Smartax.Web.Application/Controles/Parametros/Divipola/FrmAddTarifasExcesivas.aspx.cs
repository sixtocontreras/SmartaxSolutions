        protected void RadGrid1_UpdateCommand(object source, Telerik.Web.UI.GridCommandEventArgs e)
        {
            GridEditableItem editedItem = (GridEditableItem)e.Item;
            DataTable TablaDatos = new DataTable();
            TablaDatos = this.FuenteDatos.Tables["DtMunTarExcesivas"];
            TablaDatos.PrimaryKey = new DataColumn[] { TablaDatos.Columns["idmun_tarifa_excesiva"] };
            DataRow[] changedRows = TablaDatos.Select("idmun_tarifa_excesiva = " + Int32.Parse(editedItem.OwnerTableView.DataKeyValues[editedItem.ItemIndex]["idmun_tarifa_excesiva"].ToString()));
            int _IdRegistro = Int32.Parse(e.Item.OwnerTableView.DataKeyValues[e.Item.ItemIndex]["idmun_tarifa_excesiva"].ToString().Trim());

            if (changedRows.Length != 1)
            {
                e.Canceled = true;
                return;
            }

            Hashtable newValues = new Hashtable();
            e.Item.OwnerTableView.ExtractValuesFromItem(newValues, editedItem);
            changedRows[0].BeginEdit();

            try
            {
                foreach (DictionaryEntry entry in newValues)
                {
                    changedRows[0][(string)entry.Key] = entry.Value;
                }

                string CmbFormulario = (editedItem["idformulario_impuesto"].Controls[0] as RadComboBox).SelectedItem.Text;
                string CmbEstado = (editedItem["id_estado"].Controls[0] as RadComboBox).SelectedItem.Text;

                ObjMunTarExces.IdMunTarExcesiva = _IdRegistro;
                ObjMunTarExces.IdMunicipio = this.ViewState["IdMunicipio"].ToString().Trim();
                ObjMunTarExces.IdUsuario = Int32.Parse(Session["IdUsuario"].ToString().Trim());
                ObjMunTarExces.MotorBaseDatos = Session["MotorBaseDatos"].ToString().Trim();
                ObjMunTarExces.TipoProceso = 2;
                string _MsgError = "";

                if (ObjMunTarExces.AddUpMunTarExcesiva(changedRows[0], ref _IdRegistro, ref _MsgError))
                {
                    changedRows[0]["descripcion_formulario"] = CmbFormulario.ToString().Trim().ToUpper();
                    changedRows[0]["tarifa_ley2"] = changedRows[0]["tarifa_ley"].ToString().Trim() + "%";
                    changedRows[0]["tarifa_excesiva2"] = changedRows[0]["tarifa_excesiva"].ToString().Trim() + "%";
                    changedRows[0]["numero_acuerdo"] = changedRows[0]["numero_acuerdo"].ToString().Trim().ToUpper();
                    changedRows[0]["numero_articulo"] = changedRows[0]["numero_articulo"].ToString().Trim().ToUpper();
                    changedRows[0]["codigo_estado"] = CmbEstado.ToString().Trim().ToUpper();
                    this.FuenteDatos.Tables["DtMunTarExcesivas"].Rows[0].AcceptChanges();
                    this.FuenteDatos.Tables["DtMunTarExcesivas"].Rows[0].EndEdit();

                    #region MOSTRAR MENSAJE DE USUARIO
                    //Mostramos el mensaje porque se produjo un error con la Trx.
                    this.RadWindowManager1.ReloadOnShow = true;
