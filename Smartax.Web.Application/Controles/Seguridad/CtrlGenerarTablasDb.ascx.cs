        protected void RadGrid1_ItemCommand(object sender, GridCommandEventArgs e)
        {
            try
            {
                if (e.CommandName == "BtnGenerarTablas")
                {
                    #region GENERAR TABLAS DE DB PARA LOS CLIENTES
                    //--AQUI RECORREMOS EL DATATABLE DE CLIENTES
                    DataTable dtDatos = new DataTable();
                    DataSet DtSetClientes = new DataSet();
                    DtSetClientes = (DataSet)this.ViewState["_FuenteDatos"];
                    //--
                    dtDatos = DtSetClientes.Tables[0];
                    foreach (DataRow row in dtDatos.Rows)
                    {
                        string IdCliente = row["id_cliente"].ToString().Trim();
                        string AnioActual = DateTime.Now.ToString("yyyy");
                        //---
                        objTablas.IdCliente = IdCliente;
                        objTablas.Anio = AnioActual;
                        objTablas.TipoProceso = 1;
                        objTablas.MotorBaseDatos = this.Session["MotorBaseDatos"].ToString().Trim();

                        int _IdRegistro = 0;
                        string _MsgError = "";
                        if (objTablas.AddGenerarTablas(ref _IdRegistro, ref _MsgError))
                        {
                            _log.Info(_MsgError);
                        }
                        else
                        {
                            _log.Error(_MsgError);
                        }
                    }

                    #endregion
                }
                else
                {
                    //Aqui deshabilitamos el control RadWindowManager1 para que no vuelva a mostrar la ventana del Popup
                    this.RadWindowManager1.Enabled = false;
                    this.RadWindowManager1.EnableAjaxSkinRendering = false;
                    this.RadWindowManager1.Visible = false;
                }
            }
            catch (Exception ex)
            {
                #region MOSTRAR MENSAJE DE USUARIO
                //Mostramos el mensaje porque se produjo un error con la Trx.
                this.RadWindowManager1.ReloadOnShow = true;
                this.RadWindowManager1.DestroyOnClose = true;
                this.RadWindowManager1.Windows.Clear();
                this.RadWindowManager1.Enabled = true;
                this.RadWindowManager1.EnableAjaxSkinRendering = true;
                this.RadWindowManager1.Visible = true;

                RadWindow Ventana = new RadWindow();
                Ventana.Modal = true;
                string _MsgMensaje = "Error con el evento ItemCommand. Motivo: " + ex.ToString();
                Ventana.NavigateUrl = "/Controles/General/FrmMensaje.aspx?strMensaje=" + _MsgMensaje;
                Ventana.ID = "RadWindow2";
                Ventana.VisibleOnPageLoad = true;
                Ventana.Visible = true;
                Ventana.Height = Unit.Pixel(300);
                Ventana.Width = Unit.Pixel(600);
                Ventana.KeepInScreenBounds = true;
                Ventana.Title = "Mensaje del Sistema";
                Ventana.VisibleStatusbar = false;
                Ventana.Behaviors = WindowBehaviors.Close;
                this.RadWindowManager1.Windows.Add(Ventana);
                this.RadWindowManager1 = null;
                Ventana = null;
                _log.Error(_MsgMensaje.Trim());
                #endregion
            }
        }

