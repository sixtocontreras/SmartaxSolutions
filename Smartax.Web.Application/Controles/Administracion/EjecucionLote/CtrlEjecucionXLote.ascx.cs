        protected void RadGrid1_InsertCommand(object source, Telerik.Web.UI.GridCommandEventArgs e)
        {
            GridEditableItem editedItem = (GridEditableItem)e.Item;
            DataTable TablaDatos = this.FuenteDatos.Tables["DtEjecucionXLote"]; ;
            DataRow NuevaFila = TablaDatos.NewRow();
            TablaDatos.PrimaryKey = new DataColumn[] { TablaDatos.Columns["idejecucion_lote"] };
            DataRow[] TodosValores = TablaDatos.Select("", "idejecucion_lote", DataViewRowState.CurrentRows); ;

            Hashtable newValues = new Hashtable();
            e.Item.OwnerTableView.ExtractValuesFromItem(newValues, editedItem);

            try
            {
                foreach (DictionaryEntry entry in newValues)
                {
                    NuevaFila[(string)entry.Key] = entry.Value;
                }

                if ((NuevaFila != null))
                {
                    string CmbEstado = (editedItem["id_estado"].Controls[0] as RadComboBox).SelectedItem.Text;
                    string CmbCliente = (editedItem["id_cliente"].Controls[0] as RadComboBox).SelectedItem.Text;
                    string CmbEmpresa = (editedItem["id_empresa"].Controls[0] as RadComboBox).SelectedItem.Text;
                    //--
                    ObjEjecucionXLote.IdEjecucionLote = null;
                    ObjEjecucionXLote.NombreProceso = NuevaFila["nombre_proceso"].ToString().Trim().ToUpper();
                    ObjEjecucionXLote.IdEstado = NuevaFila["id_estado"].ToString().Trim().ToUpper();
                    ObjEjecucionXLote.IdEmpresa = Int32.Parse(this.Session["IdEmpresa"].ToString().Trim());
                    ObjEjecucionXLote.IdUsuario = Int32.Parse(this.Session["IdUsuario"].ToString().Trim());
                    ObjEjecucionXLote.IdRol = Int32.Parse(this.Session["IdRol"].ToString().Trim());
                    ObjEjecucionXLote.MotorBaseDatos = this.Session["MotorBaseDatos"].ToString().Trim();
                    ObjEjecucionXLote.TipoProceso = 1;

                    //--AQUI SERIALIZAMOS EL OBJETO CLASE
                    JavaScriptSerializer js = new JavaScriptSerializer();
                    string jsonRequest = js.Serialize(ObjEjecucionXLote);
                    _log.Warn("REQUEST INSERT EJECUCION POR LOTE => " + jsonRequest);

                    int _IdRegistro = 0;
                    string _MsgError = "";
                    if (ObjEjecucionXLote.AddUpEjecucionXLote(NuevaFila, ref _IdRegistro, ref _MsgError))
                    {
                        NuevaFila["idejecucion_lote"] = _IdRegistro;
                        NuevaFila["nombre_cliente"] = CmbCliente.ToString().Trim();
                        NuevaFila["nombre_proceso"] = NuevaFila["nombre_proceso"].ToString().Trim().ToUpper();
                        NuevaFila["nombre_empresa"] = CmbEmpresa.ToString().Trim();
                        NuevaFila["codigo_estado"] = CmbEstado.ToString().Trim();
                        NuevaFila["fecha_registro"] = DateTime.Now.ToString("dd-MM-yyyy hh:mm:ss");
                        this.FuenteDatos.Tables["DtEjecucionXLote"].Rows.Add(NuevaFila);

                        #region REGISTRO DE LOGS DE AUDITORIA
                        //--AQUI REGISTRAMOS EN LOS LOGS DE AUDITORIA
                        ObjAuditoria.MotorBaseDatos = this.Session["MotorBaseDatos"].ToString().Trim();
                        ObjAuditoria.IdEmpresa = Convert.ToInt32(this.Session["IdEmpresa"].ToString().Trim());
                        ObjAuditoria.IdUsuario = Convert.ToInt32(this.Session["IdUsuario"].ToString().Trim());
                        ObjAuditoria.IdTipoEvento = 2;  //--INSERT
                        ObjAuditoria.ModuloApp = "EJECUCION_POR_LOTE";
                        ObjAuditoria.UrlVisitada = Request.ServerVariables["PATH_INFO"].ToString().Trim();
                        ObjAuditoria.DescripcionEvento = jsonRequest;
                        ObjAuditoria.IPCliente = ObjUtils.GetIPAddress().ToString().Trim();
                        ObjAuditoria.TipoProceso = 1;

                        //'Agregar Auditoria del sistema
                        string _MsgErrorLogs = "";
                        if (!ObjAuditoria.AddAuditoria(ref _MsgErrorLogs))
                        {
                            _log.Error(_MsgErrorLogs);
                        }
