            TablaDatos = this.FuenteDatos.Tables["DtCuentas"];
            TablaDatos.PrimaryKey = new DataColumn[] { TablaDatos.Columns["id_cuentas_columnas_f321"] };
            DataRow[] changedRows = TablaDatos.Select("id_cuentas_columnas_f321 = " + Int32.Parse(editedItem.OwnerTableView.DataKeyValues[editedItem.ItemIndex]["id_cuentas_columnas_f321"].ToString()));
            int _IdRegistro = Int32.Parse(e.Item.OwnerTableView.DataKeyValues[e.Item.ItemIndex]["id_cuentas_columnas_f321"].ToString().Trim());

            if (changedRows.Length != 1)
            {
                e.Canceled = true;
                return;
            }

            Hashtable newValues = new Hashtable();
            e.Item.OwnerTableView.ExtractValuesFromItem(newValues, editedItem);
            changedRows[0].BeginEdit();

            try
            {
                foreach (DictionaryEntry entry in newValues)
                {
                    changedRows[0][(string)entry.Key] = entry.Value;
                }

                string _MsgError = "";

